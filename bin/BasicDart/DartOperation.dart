void main(){
   var A=10;
   var B=20;
   //Arithmetic Operation
  print("Addition Operation  ${A+B}" );
  print("Subtruction Operation  ${A-B}" );
  print("Multiplication  Operation  ${A*B}" );
  print("Division Operation  ${A/B}" );
  print("Modulus Operation  ${A%B}\n\n" );


  print("Pre and Post Increment : \n");
  // Pre Increment
  print(++A);

  //Post Increment
  print(B++);
  print(B);

  print("Pre and post decrement : \n");

  //pre decrement
  print(--A);

  //Post Decrement
  print(B--);
  print(B);

}